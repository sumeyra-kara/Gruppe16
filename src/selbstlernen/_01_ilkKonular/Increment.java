package selbstlernen._01_ilkKonular;

public class Increment {
    public static void main(String[] args) {

        int sayi = 10;
        System.out.println(sayi = sayi+3); // 13
        System.out.println(sayi);// 13

        System.out.println(sayi-=5); // 8
        System.out.println(sayi); // 8

        System.out.println(sayi++);//8 (sayi=sayi+1 yani once sayi 8 dir ama sonra 1 artiyor)
        System.out.println(sayi); // 9 yukarida 1 arttirdik ve sonra atanmisti deger




    }
}
