package tage.tag14;

public class HausaufgabeRelationenOp {

    public static void main(String[] args) {
        
        int a = 10;
        int b = 14;

        System.out.println("a b`ye esit mi: " + (a==b));
        System.out.println("a b`ye esit degil mi: " + (a!=b));
        System.out.println("a b`den büyük mü: " + (a>b));
        System.out.println("a b`den kücük mü: " + (a<b));
        System.out.println("a b`den büyük esit mi: " + (a>=b));
        System.out.println("a b`den kücük esit mi: " + (a<=b));
    }
}
