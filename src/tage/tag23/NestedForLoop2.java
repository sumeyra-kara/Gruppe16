package tage.tag23;

public class NestedForLoop2 {
    public static void main(String[] args) {

        // Multiplikation Table


        for (int n = 1; n <=10 ; n++) {
            for (int i = 1; i <=10 ; i++) {
                System.out.print(n*i + " ");
            }
            System.out.println(" ");
        }
    }
}
